    private void jButton_CreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CreateActionPerformed
        // TODO add your handling code here:
        try{
            
            DBConnect obj = new DBConnect();
            conn = obj.openConnection();        
    
            String sql = "INSERT INTO account(Name,AccountNo,AccountType,Pin,Gender,DateOfBirth,NID,Mobile,Address,Balance,SecurityCode) VALUES(?,?,?,?,?,?,?,?,?,?,?)";
            
            pst = conn.prepareStatement(sql);
            
            pst.setString(1,jTextField_Name.getText());
            pst.setString(2,jTextField_AccountNo.getText());
            pst.setString(3,(String) jComboBox_AccountType.getSelectedItem());
            pst.setString(4,jTextField_Pin.getText());
            pst.setString(5, gender);

            pst.setString(6, ((JTextField)jDateChooser_DateOfBirth.getDateEditor().getUiComponent()).getText());

            pst.setString(7,jTextField_NID.getText());
            pst.setString(8,jTextField_Mobile.getText());
            pst.setString(9,jTextField_Address.getText());     
            pst.setString(10,jTextField_Amount.getText());
            pst.setString(11,jTextField_SecurityCode.getText());
            
            pst.execute();
            Bal();
            
            JOptionPane.showMessageDialog(null, "Congratulations\n Account Created Successfully.");

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jButton_CreateActionPerformed

