    public void Bal(){
        String sql="INSERT INTO balances(Name,AccountNo,Balance) VALUES (?,?,?)";
        try{
            DBConnect obj = new DBConnect();
            conn = obj.openConnection();
            
            pst=conn.prepareStatement(sql);
            pst.setString(1,jTextField_Name.getText());
            pst.setString(2,jTextField_AccountNo.getText());
            pst.setString(3,jTextField_Amount.getText());
            pst.execute();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
